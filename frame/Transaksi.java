            String sql="update transaksi set id_transaksi='"+txtidtrans.getText()+"',id_pembeli='"+txtidpembeli.getText()+"',id_admin='"+txtidadmin.getText()+"',produk_id='"+txtidproduk.getText()+"',tgl_transaksi='"+txttanggal.getText()+"',jumlah_beli='"+txtjumlahb.getText()+"' where id_transaksi='"+txtidtrans.getText()+"'";
